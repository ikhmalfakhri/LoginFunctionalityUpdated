    public void retrieveData() {
        String q1 = "SELECT * FROM TIMETABLE_MODULES JOIN VALID_MODULES ON TIMETABLE_MODULES.MODULES = VALID_MODULES.MODULE WHERE MODULES = '" + wp.getModuleCode() + "'";

        try { //retrieve the rest
            ps = con.prepareStatement(q1);
            rs = ps.executeQuery();
            while (rs.next()) {
                String ACTIVITY = rs.getString("ACTIVITYTYPE");
                String LECTURER = rs.getString("LECTURER");
                String START_TIME = rs.getString("TIMESTART");
                String TIME_END = rs.getString("TIMEEND");
                String OCC = rs.getString("OCCURENCE");

                String tbData[] = {ACTIVITY, LECTURER, START_TIME, TIME_END, OCC};
                DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();

                tblModel.addRow(tbData);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    public void setLabel() {
        moduleShow.setText("Classes for: " + wp.getModuleCode());
    }

    public void setCreditHour() {
        String q1 = "SELECT CREDIT FROM VALID_MODULES WHERE MODULE = '" + wp.getModuleCode() + "'";

        try {
            ps = con.prepareStatement(q1);
            rs = ps.executeQuery();
            while (rs.next()){
                String OCC = rs.getString("CREDIT");
                jLabel1.setText("Credit hours: " + OCC);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

